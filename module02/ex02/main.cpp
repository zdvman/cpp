#include "Fixed.hpp"

/* ************************************************************************** */
/*                                                                            */
/*  Main function to demonstrate the functionality of the Fixed class.        */
/*                                                                            */
/* ************************************************************************** */
int	main( void ) {
/* ************************************************************************** */
/*                                                                            */
/*  Create a Fixed object `a` initialized to 0 using the default constructor. */
/*  Create a constant Fixed object `b` initialized by multiplying two Fixed   */
/*  objects: `5.05f` and `2`.                                                 */
/*                                                                            */
/* ************************************************************************** */
	Fixed a;
	Fixed const b( Fixed( 5.05f ) * Fixed( 2 ) );

/* ************************************************************************** */
/*                                                                            */
/*  Output the current value of `a`, which should be 0.                       */
/*                                                                            */
/* ************************************************************************** */
	std::cout << a << std::endl;

/* ************************************************************************** */
/*                                                                            */
/*  Pre-increment `a` (increment `a` first, then output the new value).       */
/*                                                                            */
/* ************************************************************************** */
	std::cout << ++a << std::endl;

/* ************************************************************************** */
/*                                                                            */
/*  Output the current value of `a` after the pre-increment.                  */
/*                                                                            */
/* ************************************************************************** */
	std::cout << a << std::endl;

/* ************************************************************************** */
/*                                                                            */
/*  Post-increment `a` (output the current value first, then increment `a`).  */
/*                                                                            */
/* ************************************************************************** */
	std::cout << a++ << std::endl;

/* ************************************************************************** */
/*                                                                            */
/*  Output the current value of `a` after the post-increment.                 */
/*                                                                            */
/* ************************************************************************** */
	std::cout << a << std::endl;

/* ************************************************************************** */
/*                                                                            */
/*  Output the value of the constant Fixed object `b`.                        */
/*  This should display the result of the multiplication `5.05f * 2`.         */
/*                                                                            */
/* ************************************************************************** */
	std::cout << b << std::endl;

/* ************************************************************************** */
/*                                                                            */
/*  Output the maximum value between `a` and `b` using the `Fixed::max`       */
/*  static method. Since `b` was initialized with a larger value, it should   */
/*  be the output.                                                            */
/*                                                                            */
/* ************************************************************************** */
	std::cout << Fixed::max( a, b ) << std::endl;

/* ************************************************************************** */
/*                                                                            */
/*  Return 0 to indicate successful execution.                                */
/*                                                                            */
/* ************************************************************************** */
	return 0;
}
